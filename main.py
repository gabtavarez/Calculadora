# Numeros Inteiros (int)
# Inteiros são números inteiros, positivos ou negativos, sem casas decimais.
x = int("10")
print(x * 5)

# Texto (str) String
nome = str(123)
print("Olá, " + nome)

# float → Número com casas decimais
nota = float(7.5)
print(nota + 2.5)

# Valor booleano (Verdadeiro ou Falso)
print(bool(1)) #true
print(bool(0)) #false
print(bool("")) #false | string vazia
print(bool("oi")) #true | qualquer texto tem valor true